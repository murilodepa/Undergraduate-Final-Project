package com.api.tcc.utils;

/**
 * @author Murilo de Paula Araujo
 */
public class IndexAndName {
    private long id;
    private String name;

    public IndexAndName(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
